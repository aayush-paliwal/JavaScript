//It will stop execution of the loop when condition will becomes true 
for(let i=0;i<=8;i++){
    if(i==4){
      break      
    }
    console.log(i)
  }
  console.log("Hello!!!")
  console.log()
  
  
  // Continue
  
  // It will skip execution of loop for i==4
  for(let i=0;i<=8;i++){
    if(i==4){
      continue      
    }
    console.log(i)
  }
