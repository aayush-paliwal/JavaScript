// Operators in JavaScript
// Arithmetic Operator

let a = 34
let b = 3
let c = 20
let d = 12

console.log("a+b is: ", a + b)
console.log("a-b is: ", a - b)
console.log("a*b is: ", a * b)
console.log("a/b is: ", a / b)
console.log("a**b is: ", a ** b)
console.log("a%b is: ", a % b)

// Increment Operator
console.log("a++ is: ", a++)
console.log(a)

console.log("++b is: ", ++b)

// Decrement Operator
console.log("c-- is: ", c--)
console.log(c)

console.log("--d is: ", --d)


// Assignment Operator
let e = 4
let f = 5

e += f     // +=,-=,/=,*=,%=,**=
console.log("e is: ", e)
e = f
console.log(e)
console.log()

// Comparison Operator
let num1 = 5
let num2 = 6
let num3 = "5"

console.log("num1==num2 is:", num1 == num2)
console.log("num1!=num2 is:", num1 != num2)
console.log("num1==num3 is:", num1 == num3)
console.log("num1!=num3 is:", num1 != num3)

console.log("num1===num2 is:", num1 === num2)
console.log("num1!==num2 is:", num1 !== num2)
console.log("num1===num3 is:", num1 === num3)
console.log("num1!==num3 is:", num1 !== num3)
console.log()

// Logical Operators
console.log(5)
console.log(!5)

